/*
 * This program is a calculator that subtracts the sum of two numbers,
 * It multiplies the sum of three numbers,
 * And divides the sum of four numbers
 */

public class Calculator {
    public int sub(int a, int b){
        return a-b;
    }

    public int mult(int x, int y, int z){
        return x*y*z;
    }

    public int div(int c, int d, int e, int f){
        return c/d/e/f;
    }

}
